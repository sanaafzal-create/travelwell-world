/**
 * TravelWell OS v5.0 — Global Emergency Numbers
 * Emergency Safety System · Offline Bundle
 *
 * This file is compiled INTO the client JavaScript bundle. It requires
 * ZERO network calls. It works with no WiFi, no data signal, no SIM credit.
 * The only requirement is that the app was opened at least once.
 *
 * Coverage: 117 countries + major territories.
 * Source: ICAO, ITU, and national emergency services directories.
 * Last verified: May 2026.
 *
 * 112 is the international standard and works as a fallback almost
 * everywhere — including from locked phones and phones with no SIM.
 *
 * Format: ISO 3166-1 alpha-2 country code → EmergencyNumbers
 *
 * Zero Drift Policy: data updates to this file are data changes, not
 * structural changes — no OS Architect sign-off required for number updates.
 */

