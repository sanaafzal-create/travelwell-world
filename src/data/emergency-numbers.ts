/**
 * Look up emergency numbers by ISO country code.
 * Always returns something — falls back to universal 112.
 */
export function getEmergencyNumbers(isoCode: string): EmergencyNumbers {
  return (
    GLOBAL_EMERGENCY_NUMBERS[isoCode.toUpperCase()] ?? {
      country: 'Unknown — use universal number',
      ambulance: UNIVERSAL_EMERGENCY,
      police: UNIVERSAL_EMERGENCY,
      universal_fallback: '112',
      notes: 'Country not found. 112 connects to emergency services in 190+ countries.',
    }
  );
}

